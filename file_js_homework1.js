console.log('number' + 3 + 3); /* number33, 
коли один з операндів є строковим, 
JavaScript перетворює інший операнд на стороку, 
число '3' перетворюється на строку '"3"',
оператор '+' використовується для конкатинації строк, '"number" + "3"' => "number3", 
строка '"number3"' і число '3' перетворюється на строку '"3"', 
'"number3" + "3"' => "number33" */

console.log(null + 3); /* 3, при використанні математичних операцій 'null' перетворюється на '0', '0 + 3 = 3'*/

